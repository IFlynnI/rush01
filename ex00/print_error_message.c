#include <unistd.h>

int	print_error_message(char *str)
{
	int	i;

	i = 0;
	write(2, "\033[1;31m", 7);
	write(2, "error: ", 7);
	while (str[i])
	{
		write(2, &str[i], 1);
		i++;
	}
	write(2, "\033[0m", 4);
	write(2, "\n", 1);
	return (0);
}
